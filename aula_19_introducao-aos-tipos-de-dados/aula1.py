"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Everton Luis')
print('Everton "Luis"')

# Aspas duplas
print("Everton Luis")
print("Everton 'Luis'")

# Escape
print("Everton \"Luis\"")

# r
print(r"Everton \"Luis\"")
